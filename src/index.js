const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    
    const arrWords = expr.split('**********');
  
    let res = '';
    arrWords.forEach((word,ind) => {
        res += word.split(/(?=(?:\d{10})+$)/).map((letter) => MORSE_TABLE[letter.split(/(?=(?:\d{2})+$)/).map((item) => 
                ((item == '00') ? '' : (item == '10') ? '.' : '-')).join('')]
            ).join('') + ((ind < (arrWords.length - 1)) ? ' ' : '');
        }
    );
    return res;       
    
}

module.exports = {
    decode
}